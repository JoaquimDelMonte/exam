# Étapes de calcul pour la matrice de covariance

# 3) Matrice de Covariance :
# V = (1 / (n-1)) * (A_nd^T * A_nd) = (1 / 3)

# Matrice A_nd :
# [
#   -0.82   -0.82    0    1.64
#   -1.2    -0.15    0.19  1.51
# ]
# Astd :
# [
#   -0.82  -1.2
#   -0.82  -0.15
#    0      0.19
#    1.64   1.51
# ]
# Produit matriciel (A_nd^T * A_nd) :
# [
#   (-0.82)^2 + (-0.82)^2 + 0 + (1.64)^2    //  (-0.82)*(-1.2) + (-0.82)*(-0.15) + 0*(0.19) + (1.64)*(1.51)
#   (-1.2)*(-0.82) + (-0.15)*(-0.82) + 0*(0) + (1.51)*(1.64) // (-1.2)^2 + (-0.15)^2 + (0.19)^2 + (1.51)^2
# ]
# =
# [
#   4.034   3.870
#   3.870   4.006
# ]

# Calcul :
# V = (1 / 3) *
# [
#   4.034   3.870
#   3.870   4.006
# ]

# Matrice finale :
# V ≈
# [
#   1.34   1.29
#   1.29   1.34
# ]
